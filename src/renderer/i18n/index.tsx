// ===== React Context for i18n =====
// This is the KEY fix: using React context ensures that when language changes,
// ALL components that call t() will re-render

interface I18nContextValue {
  lang: Lang;
  t: (key: string) => string;
  setLang: (lang: Lang) => void;
}

const I18nContext = createContext<I18nContextValue>({
  lang: 'zh-CN',
  t: (key: string) => key,
  setLang: () => {},
});

// Provider component — must wrap the entire App
export const I18nProvider: React.FC<{ children: React.ReactNode; initialLang?: Lang }> = ({ children, initialLang = 'zh-CN' }) => {
  const [lang, setLang] = useState<Lang>(initialLang);

  const t = useCallback((key: string): string => {
    return translations[lang]?.[key] ?? translations['zh-CN']?.[key] ?? key;
  }, [lang]);

  return (
    <I18nContext.Provider value={{ lang, t, setLang }}>
      {children}
    </I18nContext.Provider>
  );
};

// Hook for consuming i18n in any component
export function useI18n() {
  return useContext(I18nContext);
}

// Standalone translate function (for use outside React, or before Provider mounts)
export function translateStatic(key: string, lang: Lang = 'zh-CN'): string {
  return translations[lang]?.[key] ?? translations['zh-CN']?.[key] ?? key;
}

export default I18nContext;
